CREATE OR REPLACE VIEW OSP$NOTIFICATION_TYPE AS SELECT
  NOTIFICATION_TYPE_ID NOTIFICATION_NUMBER, 
  MODULE_CODE, 
  ACTION_CODE, 
  DESCRIPTION, 
  SUBJECT, 
  MESSAGE, 
  PROMPT_USER, 
  SEND_NOTIFICATION,
  SYSTEM_GENERATED,
  UPDATE_USER, 
  UPDATE_TIMESTAMP 
  FROM NOTIFICATION_TYPE;